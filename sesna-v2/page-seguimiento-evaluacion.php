        <h1 style="text-aligncenter"><b style="font-size:30px;">Indicadores Nacionales</b></h1>

        <h1>Una de las herramientas fundamentales del SiSE serán los 10 Indicadores Nacionales, que se asociaron a cada
            uno de los Objetivos Específicos de la PNA. Además de constituirse como aliados para el seguimiento, éstos
            ofrecerán elementos de cara a las posteriores evaluaciones que se habrán de llevar a cabo.</h1>


        <h1>La construcción de estos indicadores fue resultado de un amplio consenso y diálogo técnico tanto con
            instituciones públicas integrantes e involucradas en el SNA, como con instituciones académicas. Esta
            dinámica permitió fortalecer técnicamente las métricas, al mismo tiempo que se aseguró la construcción de
            indicadores integrales a partir de la inclusión de diversos de enfoques.</h1>

        <h1>Los Indicadores Nacionales se aprobaron en la Primera Sesión Extraordinaria 2024 del CC-SNA, celebrada el 12
